def run_workflow():

    create_folder()


    try:

        with open(WORKFLOW_FILE, "r") as f:
            data = json.load(f)


    except:

        print("❌ No workflows found")

        return



    print("\n⚡ AVAILABLE WORKFLOWS")


    for index, workflow in enumerate(data, 1):

        print(index, ".", workflow["name"])



    choice = int(input("\nSelect workflow: "))


    selected = data[choice - 1]


    print("\n🚀 EXECUTING:", selected["name"])



    for step in selected["steps"]:


        print("\n▶ Running:", step)



        if "story" in step.lower():

            stories.auto_create_story(
                "META ALPHA Generated Story",
                "Sci-Fi",
                "A futuristic AI war where humans and machines fight for control."
            )



        elif "character" in step.lower():

            print("👥 Character generation module ready")



        elif "prompt" in step.lower():

            prompts.auto_create_prompt(
                "META ALPHA Cinematic Prompt",
                "Video",
                "A futuristic city with advanced AI machines and cinematic lighting."
            )



        elif "save" in step.lower():

            files.auto_save_project(
                "META_ALPHA_Project",
                "META ALPHA Creation\n\nGenerated by BOBBY AI STUDIO v2.8"
            )



        else:

            print("⚙️ Task processed")



    print("\n🎉 WORKFLOW AUTOMATION COMPLETED!")