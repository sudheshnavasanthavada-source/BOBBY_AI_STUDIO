import json
import os
from datetime import datetime

import stories
import prompts
import files


WORKFLOW_FILE = "data/workflows.json"


def create_folder():

    if not os.path.exists("data"):
        os.makedirs("data")



def save_workflow(name, steps):

    create_folder()

    workflow = {

        "name": name,
        "steps": steps,
        "created": str(datetime.now())

    }


    try:

        with open(WORKFLOW_FILE, "r") as f:
            data = json.load(f)

    except:

        data = []


    data.append(workflow)


    with open(WORKFLOW_FILE, "w") as f:

        json.dump(data, f, indent=4)


    print("✅ Workflow Saved Successfully!")



def view_workflows():

    create_folder()

    try:

        with open(WORKFLOW_FILE, "r") as f:
            data = json.load(f)


        print("\n⚡ SAVED WORKFLOWS")


        for index, workflow in enumerate(data, 1):

            print("\n", index, ".", workflow["name"])
            print("Steps:", workflow["steps"])
            print("Created:", workflow["created"])


    except:

        print("❌ No workflows available")



def create_workflow():

    name = input("\nEnter workflow name: ")

    steps = []


    print("\nAdd workflow steps")
    print("Type 'done' to finish")


    while True:

        step = input("Step: ")


        if step.lower() == "done":

            break


        steps.append(step)


    save_workflow(name, steps)



